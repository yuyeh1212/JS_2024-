// 題目一：在使用 axios 串接 API 的時候，如果 HTTP 狀態碼顯示為 403，則代表有成功接收到資料，請問是正確還是錯誤？如果答案為錯誤，則狀態碼 403 代表什麼訊息，以及成功接收到資料的 HTTP 狀態碼應該為何？
// 解答:錯誤。
// HTTP 狀態碼 403 代表禁止存取（Forbidden）。這表示伺服器已經接收到請求，但拒絕執行，通常是因為用戶端沒有適當的權限來存取資源。因此，403 並不表示有成功接收到資料。
// 成功接收到資料的 HTTP 狀態碼：
// 當資料請求成功時，應該是返回 200 狀態碼，表示請求成功。

// 題目二：請問「用戶端錯誤」以及「伺服器端錯誤」的 HTTP 狀態碼各自位於哪個數字區間？可否各自舉一個例子說明？
// 解答:用戶端錯誤的 HTTP 狀態碼範圍是 400-499。
// 範例：404 Not Found，表示伺服器無法找到請求的資源。
// 伺服器端錯誤的 HTTP 狀態碼範圍是 500-599。
// 範例：500 Internal Server Error，表示伺服器在處理請求時發生了內部錯誤。
